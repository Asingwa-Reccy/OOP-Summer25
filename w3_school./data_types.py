# Creates variables of the following types
# Integer
x = 100
print (x)
print(type(x))

# Float
days = 15.4
print(days)
print(type(days))

# String
x = "hello world!"
print(x)
print(type(x))

# Boolean
x = False
print(x)
print(type(x))

# Tuple
continent = ("Africa", "Europe", "America")
print(continent)
print(type(continent))

# List
girls = ["anayah", "jamaiah", "nayla"]
print(girls)
print(type(girls))

# Dictionariy
computers = {"HP": 25, "Lenovo": 15, "Apple" : 20}
print(computers)
print(type(computers))

# Set
fruits = {"mangoes", "bananas", "watermellons"}
print(fruits)
print(type(fruits))

